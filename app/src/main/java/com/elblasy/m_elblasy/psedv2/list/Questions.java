package com.elblasy.m_elblasy.psedv2.list;

public class Questions {

    private String name;
    private String question;

    public Questions(String name, String question) {
        this.name = name;
        this.question = question;
    }

    public String getName() {
        return name;
    }

    public String getQuestion() {
        return question;
    }
}
